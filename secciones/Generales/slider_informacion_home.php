<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><b><span lang=ES style='line-height:115%;color:#404040'>Introducción</span></b></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><span lang=ES style='line-height:115%;color:#404040'>El Laboratorio de
Conservación del Museo Nacional de Antropología (MNA) de la Ciudad de México es
el encargado de conservar<a href="#_ftn3" name="_ftnref3" title=""><span
class=MsoFootnoteReference><span class=MsoFootnoteReference><span lang=ES-MX
style='font-size:12.0pt;line-height:150%;font-family:Arial;color:#404040'>[3]</span></span></span></a>
y restaurar<a href="#_ftn4" name="_ftnref4" title=""><span
class=MsoFootnoteReference><span class=MsoFootnoteReference><span lang=ES-MX
style='font-size:12.0pt;line-height:150%;font-family:Arial;color:#404040'>[4]</span></span></span></a>
sus colecciones arqueológicas, etnográficas e históricas, y en coordinación con
el INBA, también la obra artística que resguarda dicho recinto. Este acervo se
caracteriza por la gran variedad de materiales que lo conforman, como piedra,
pintura, plumas, metales, madera y cerámica, entre otros. </span></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><span lang=ES style='line-height:115%;color:#404040'>En el campo de la
conservación es indispensable el estudio previo de las piezas con el fin de
recabar toda la información necesaria –que abarca desde los aspectos propios de
la historia de cada objeto hasta su tecnología, el material con el que están
elaborados, etc.–, para después desarrollar estrategias y metodologías de
conservación o de restauración adecuadas a cada obra, las cuales se basan en la
ciencia de los materiales.&nbsp; </span></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><span lang=ES style='line-height:115%;color:#404040'>Para lograr este
cometido, el laboratorio realiza una serie de estudios científicos mediante
diversas herramientas y métodos de análisis, incluidos algunos de primera
aproximación al objeto.</span></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><span lang=ES style='line-height:115%;color:#404040'>&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><b><span lang=ES style='line-height:115%;color:#404040'>Caso de estudio:
Escultura mexica de Xochipilli, análisis de la policromía</span></b></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><span lang=ES style='line-height:115%;color:#404040'>Como parte de los
trabajos multidisciplinarios que se realizan en el MNA, en coordinación con la
curaduría de la Sala Mexica se estudió la policromía de la escultura de Xochipilli,
con la finalidad de complementar la investigación realizada por otras
disciplinas.<a href="#_ftn5" name="_ftnref5" title=""><span
class=MsoFootnoteReference><span class=MsoFootnoteReference><span lang=ES-MX
style='font-size:12.0pt;line-height:150%;font-family:Arial;color:#404040'>[5]</span></span></span></a>
</span></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><span lang=ES style='line-height:115%;color:#404040'>&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><b><span lang=ES style='line-height:115%;color:#404040'>Objetivos </span></b></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><span lang=ES style='line-height:115%;color:#404040'>El propósito de
dicho estudio fue conocer el estado de conservación de la decoración para
lograr una propuesta de la distribución y la estratigrafía<a href="#_ftn6"
name="_ftnref6" title=""><span class=MsoFootnoteReference><span
class=MsoFootnoteReference><span lang=ES-MX style='font-size:12.0pt;line-height:
150%;font-family:Arial;color:#404040'>[6]</span></span></span></a> de la
policromía<a href="#_ftn7" name="_ftnref7" title=""><span
class=MsoFootnoteReference><span class=MsoFootnoteReference><span lang=ES-MX
style='font-size:12.0pt;line-height:150%;font-family:Arial;color:#404040'>[7]</span></span></span></a>.</span></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><span lang=ES style='line-height:115%;color:#404040'>&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><b><span lang=ES style='line-height:115%;color:#404040'>Metodología
aplicada al caso de estudio y resultados</span></b></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><b><i><span lang=ES-MX style='line-height:115%;color:#404040'>Investigación
documental </span></i></b></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><span lang=ES-MX style='line-height:115%;color:#404040'>La consulta de
fuentes es básica para manejar adecuadamente los objetos arqueológicos que
pudieran ser expuestos en el museo, ya que por lo general han sufrido diversas
modificaciones, desde su manufactura y su extracción de la matriz arqueológica,
hasta su manipulación, restauración y exhibición. Estas modificaciones pudieron
ser fruto de operaciones que buscaban preservar y facilitar la comprensión del
objeto exhibido. Para los conservadores es relevante acudir a las fuentes
escritas y gráficas<a href="#_ftn8" name="_ftnref8" title=""><span
class=MsoFootnoteReference><span class=MsoFootnoteReference><span lang=ES-MX
style='font-size:12.0pt;line-height:150%;font-family:Arial;color:#404040'>[8]</span></span></span></a>
(como fotografías, esquemas y dibujos), ya que pueden aportar valiosa
información sobre dichas modificaciones.</span></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><span lang=ES style='line-height:115%;color:#404040'>Gracias a estudios
similares<a href="#_ftn9" name="_ftnref9" title=""><span
class=MsoFootnoteReference><span class=MsoFootnoteReference><span lang=ES-MX
style='font-size:12.0pt;line-height:150%;font-family:Arial;color:#404040'>[9]</span></span></span></a>
al que ocupa esta investigación,<a href="#_ftn10" name="_ftnref10" title=""><span
class=MsoFootnoteReference><span class=MsoFootnoteReference><span lang=ES-MX
style='font-size:12.0pt;line-height:150%;font-family:Arial;color:#404040'>[10]</span></span></span></a>
sabemos que en las esculturas y en los relieves mexicas los materiales más
utilizados fueron las rocas de tipo volcánico: andesita y basalto; además, esos
estudios comprueban que era común que tales obras estuvieran policromadas.</span></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><span lang=ES style='line-height:115%;color:#404040'>En cuanto a los
colores que conformaban la paleta mexica, ahora sabemos que los artistas
antiguos utilizaron los mismos pigmentos tanto en la escultura como en la
pintura mural,<a href="#_ftn11" name="_ftnref11" title=""><span
class=MsoFootnoteReference><span class=MsoFootnoteReference><span lang=ES-MX
style='font-size:12.0pt;line-height:150%;font-family:Arial;color:#404040'>[11]</span></span></span></a>
predominando los de naturaleza mineral. La paleta cromática se componía
principalmente de blanco, ocre, rojo, negro y azul. Estos pigmentos se
caracterizan por su naturaleza inorgánica (tierras, óxidos, etc.), híbrida
(como el añil), y orgánica (carbón vegetal, entre otros).</span></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><span lang=ES style='line-height:115%;color:#404040'>La base de
preparación, o estuco, estaba compuesta de carbonato de calcio, mientras que
los aglutinantes<a href="#_ftn12" name="_ftnref12" title=""><span
class=MsoFootnoteReference><span class=MsoFootnoteReference><span lang=ES-MX
style='font-size:12.0pt;line-height:150%;font-family:Arial;color:#404040'>[12]</span></span></span></a>,
según las fuentes documentales, procedían de gomas vegetales, proteínas
animales, aceites, ceras o resinas y azúcares,<a href="#_ftn13" name="_ftnref13"
title=""><span class=MsoFootnoteReference><span class=MsoFootnoteReference><span
lang=ES-MX style='font-size:12.0pt;line-height:150%;font-family:Arial;
color:#404040'>[13]</span></span></span></a> posiblemente de un mucílago<a
href="#_ftn14" name="_ftnref14" title=""><span class=MsoFootnoteReference><span
class=MsoFootnoteReference><span lang=ES-MX style='font-size:12.0pt;line-height:
150%;font-family:Arial;color:#404040'>[14]</span></span></span></a> de
orquídea. En la tabla 1 se observan los distintos pigmentos reportados para el
monolito de Tlaltecuhtli, en el que fue posible determinar la composición
química y cristalográfica que más adelante retomaremos como referencia para
proponer una aproximación comparativa para los restos encontrados en el de
Xochipilli.</span></p>

