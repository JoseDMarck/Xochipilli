derivan en deterioros considerables.</span></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><span lang=ES style='line-height:115%;color:#404040'>&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><b><span lang=ES style='line-height:115%;color:#404040'>Conclusiones</span></b></p>

<p class=MsoNormal style='text-align:justify;line-height:115%'><span
lang=ES-MX style='line-height:115%;color:#404040'>Este trabajo es una
aproximación a la presencia de los materiales que decoran la escultura mexica
de Xochipilli. Para su realización fue fundamental el uso de diferentes métodos
analíticos y la investigación documental, lo que demuestra la importancia de los
estudios multidisciplinares aplicados al patrimonio. Los análisis aportaron
información de gran valor histórico-artístico y permitieron elaborar una
propuesta acerca de los materiales empleados en la policromía. </span></p>

<p class=MsoNormal style='text-align:justify;line-height:115%'><span
lang=ES-MX style='line-height:115%;color:#404040'>La paleta de tonos coincide
con los reportados para la cultura mexica, al igual que con la técnica de
manufactura sugerida en otros estudios similares. La metodología aplicada nos
ayudó a comprender la técnica de manufactura usada para su decoración y a
desarrollar la siguiente propuesta: encontramos que la base de preparación
estaba presente en las partes más profundas de la talla<a href="#_ftn30"
name="_ftnref30" title=""><span class=MsoFootnoteReference><span
class=MsoFootnoteReference><span lang=ES-MX style='font-size:12.0pt;line-height:
150%;font-family:Arial;color:#404040'>[30]</span></span></span></a> y en los
poros de la piedra de toda la escultura y de la base. Por ello, podemos sugerir
que en algún momento ésta se aplicó de manera homogénea, y que posteriormente
la obra perdió gran parte de ella. </span></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><span lang=ES-MX style='line-height:115%;color:#404040'>Después de
colocar la base de preparación, se aplicó el pigmento color ocre en ciertas
zonas, en este caso para delinear algunos motivos, como las flores; después se
aplicó el color rojo en las partes restantes.&nbsp; </span></p>

<p class=MsoNormal style='margin-bottom:6.0pt;text-align:justify;line-height:
115%'><span lang=ES-MX style='line-height:115%;color:#404040'>Por otro lado,
los estudios hechos a otras obras mexicas sugieren que también existió la
aplicación directa de pigmento sin base de preparación. Con los estudios
realizados en Xochipilli, como ya mencionamos, se observa que predomina el
color rojo, pero no es concluyente asegurar que debajo de él no conserve una
base de preparación. Lo que puede inferirse es que en caso de que estudios
posteriores determinaran la aplicación directa del rojo sobre la roca, la
presencia de la base de preparación blanca serviría para afirmar que en algún
momento de su primera historia la pieza fue retocada, probablemente para
mantenimiento </span><span lang=ES-MX style='line-height:115%;color:#404040'>(imagen
7).</span><span lang=ES-MX style='line-height:115%;color:#404040'> </span></p>

<p class=MsoNormal style='text-align:justify;line-height:115%'><span
lang=ES-MX style='line-height:115%;color:#404040'>Por último, esta
investigación no es determinante para caracterizar la composición de los
pigmentos, pero sí pudo definirse con mayor exactitud su distribución y se
obtuvieron datos que dan soporte a la propuesta de la manera en que fueron
aplicados. </span></p>

<p class=MsoNormal style='text-align:justify;line-height:115%'><span
lang=ES-MX style='line-height:115%;color:#404040'>Siendo apenas una primera
aproximación a la técnica de manufactura, vale agregar que se requieren otro
tipo de análisis instrumentales que complementen y corroboren con datos
científicos lo que aquí se plantea. </span></p>

