<p class=MsoNormal style='text-align:justify'><span lang=ES-MX
style='line-height:150%;color:#404040'>La escultura de Xochipilli posee muchos
rasgos y atributos que en seguida atrapan la mirada. A un tiempo, tiene otros
que después de unos cuantos minutos de observación pausada, comienzan a saltar
a la vista generando asombro, curiosidad y cada vez más preguntas. </span></p>

<p class=MsoNormal style='text-align:justify'><span lang=ES-MX
style='line-height:150%;color:#404040'>Uno de estos rasgos cautivantes y
también característicos de esta escultura es la tonalidad roja que la baña casi
por entero como un resplandor. A más tiempo se le contempla, la piedra va
revelando poco a poco su color, y en algunos resquicios y pliegues, se
manifiesta con toda claridad.</span></p>

<p class=MsoNormal style='text-align:justify'><span lang=ES-MX
style='line-height:150%;color:#404040'>Como parte del afán de desmenuzar lo más
posible todas las cualidades y características de esta obra maestra de
tradición escultórica chalca, el Laboratorio de Conservación del Museo Nacional
de Antropología, encargado del estudio, la conservación y la restauración de su
vasta colección, llevó a cabo un análisis exhaustivo de la policromía de la
escultura de Xochipilli. Su objetivo fue buscar minuciosamente los pigmentos
albergados en la superficie y poros de la piedra de la escultura, así como su
tipo, su fecha probable y método de aplicación y su estado de conservación. Sus
resultados son muy reveladores e interesantes, ya que aportan al entendimiento
de esta emblemática pieza y nos permiten imaginarnos cómo pudo haber lucido en
su momento de creación y de mayor esplendor.</span></p>

<p class=MsoNormal style='text-align:justify'><span
lang=ES-MX style='line-height:150%;color:#404040'>A través de esta plataforma
ponemos a disposición este estudio técnico llevado a cabo por el Laboratorio de
Conservación del MNA para quienes deseen ahondar en el conocimiento de este
aspecto de Xochipilli, atributo también de la riqueza en la representación de
la escultura mexica. </span></p>

